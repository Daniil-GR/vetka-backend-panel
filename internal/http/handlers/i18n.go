func NormalizeLocale(value string) Locale {
	switch strings.ToLower(strings.TrimSpace(value)) {
	case string(LocaleEN):
		return LocaleEN
	default:
		return LocaleRU
	}
}

func ResolveLocale(r *http.Request) Locale {
	if r == nil {
		return LocaleRU
	}
	cookie, err := r.Cookie(languageCookieName)
	if err != nil {
		return LocaleRU
	}
	return NormalizeLocale(cookie.Value)
}

func Translate(locale Locale, key string) string {
	if localized, ok := translations[NormalizeLocale(string(locale))][key]; ok && localized != "" {
		return localized
	}
	if localized, ok := translations[LocaleRU][key]; ok && localized != "" {
		return localized
	}
	return key
}
